import { createHash } from 'node:crypto';
import { readdir, readFile, writeFile } from 'node:fs/promises';
import { join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = fileURLToPath(new URL('../public/assets/', import.meta.url));
const output = fileURLToPath(new URL('../public/asset-urls.js', import.meta.url));

async function filesIn(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const nested = await Promise.all(entries.map(async (entry) => {
    const target = join(directory, entry.name);
    return entry.isDirectory() ? filesIn(target) : [target];
  }));
  return nested.flat();
}

const files = await filesIn(root);
const versions = Object.fromEntries(await Promise.all(files.map(async (file) => {
  const bytes = await readFile(file);
  const path = `assets/${relative(root, file).replaceAll('\\', '/')}`;
  return [path, createHash('sha256').update(bytes).digest('hex').slice(0, 16)];
})));

const source = `// Generated by tools/generate-asset-urls.mjs. Do not edit by hand.\nconst versions = Object.freeze(${JSON.stringify(versions, null, 2)});\n\nexport function assetUrl(path) {\n  const cleanPath = path.replace(/^\\/+/, '');\n  const version = versions[cleanPath];\n  return version ? \`/\${cleanPath}?v=\${version}\` : \`/\${cleanPath}\`;\n}\n`;
await writeFile(output, source);
